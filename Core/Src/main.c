void PrintArray(uint8_t *data_arr)
{
    int debval;

    int denominator = sizeof(data_arr[0]);

    if (denominator > 0) {
        debval = (sizeof(data_arr) / denominator);
        for (int i = 0; i < debval; i++) {
            // printf("%lf\n",foo[i]);
            printf("%#x ", data_arr[i]);
        }
    }
    printf("\n");
}




